import java.util.*;
public class String_Multiply{
    public static void main(String[] args){
        Scanner sc=new Scanner(System.in);
        String s1=sc.next();
        String s2=sc.next();
        long a=Long.parseLong(s1);
        long b=Long.parseLong(s2);
        
        System.out.println(a*b);
    }
}